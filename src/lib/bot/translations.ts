export type Language = keyof typeof translations;

export function t(key: keyof typeof translations['es'], lang: Language, params?: Record<string, string>): string {
  const language = translations[lang] || translations['es'];
  let text = (language as any)[key] || (translations['es'] as any)[key] || key;
  
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  
  return text;
}
